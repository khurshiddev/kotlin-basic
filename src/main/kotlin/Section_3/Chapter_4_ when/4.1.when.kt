package Section_3.`Chapter_4_ when`

fun main() {
    val dayOfWeek = 5

    when (dayOfWeek) {
        1 -> {
            println("dushanba")
        }

        2 -> {
            println("dushanba")
        }

        3 -> {
            println("seshanba")
        }

        4 -> {
            println("payshanba")
        }

        5 -> {
            println("juma")
        }

        6 -> {
            println("shanba")
        }

        7 -> {
            println("yakshanba")
        }
        else ->{
            println("bu yerda hafta kunlari yo'q")
        }

    }
}